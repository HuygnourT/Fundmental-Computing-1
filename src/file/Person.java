package file;

public class Person {
	String name = "";
	String gender = "";
	public void setName(String name)
	{
		this.name = name;
	}
	public void setGender(String gender)
	{
		this.gender = gender;
	}
	public String getName()
	{
		return name;
	}
	public String getGender()
	{
		return gender;
	}
}
